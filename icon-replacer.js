// Fonction pour remplacer les emojis par des icônes SVG
function replaceEmojisWithIcons() {
    // Remplacer les emojis dans tout le document
    Object.keys(icons).forEach(emoji => {
        const elements = document.querySelectorAll(`*:not(script):not(style)`);
        elements.forEach(element => {
            if (element.innerHTML && element.innerHTML.includes(emoji)) {
                element.innerHTML = element.innerHTML.replace(new RegExp(emoji, 'g'), icons[emoji]);
            }
        });
    });
}

// Fonction pour remplacer les emojis dans un élément spécifique
function replaceEmojisInElement(element) {
    if (!element) return;
    
    Object.keys(icons).forEach(emoji => {
        if (element.innerHTML && element.innerHTML.includes(emoji)) {
            element.innerHTML = element.innerHTML.replace(new RegExp(emoji, 'g'), icons[emoji]);
        }
    });
}

